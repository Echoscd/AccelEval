static inline int maxi3(int a, int b, int c) {
    int m = (a > b) ? a : b;
    return (m > c) ? m : c;
}

void solution_init(int seq_len, int penalty, const int* h_seq_a, const int* h_seq_b) {
    g_seq_len = seq_len;
    g_penalty = penalty;
    g_seq_a = (int*)malloc((size_t)seq_len * sizeof(int));
    g_seq_b = (int*)malloc((size_t)seq_len * sizeof(int));
    memcpy(g_seq_a, h_seq_a, (size_t)seq_len * sizeof(int));
    memcpy(g_seq_b, h_seq_b, (size_t)seq_len * sizeof(int));
    g_dp = (int*)malloc((size_t)(seq_len + 1) * (size_t)(seq_len + 1) * sizeof(int));
}

void solution_compute(int* h_score_out) {
    const int n = g_seq_len;
    const int stride = n + 1;
    for (int i = 0; i <= n; ++i) g_dp[i * stride] = -i * g_penalty;
    for (int j = 0; j <= n; ++j) g_dp[j] = -j * g_penalty;

    for (int i = 1; i <= n; ++i) {
        const int ai = g_seq_a[i - 1];
        for (int j = 1; j <= n; ++j) {
            const int bj = g_seq_b[j - 1];
            const int match = g_dp[(i - 1) * stride + (j - 1)] + BLOSUM62[ai][bj];
            const int del   = g_dp[(i - 1) * stride + j] - g_penalty;
            const int ins   = g_dp[i * stride + (j - 1)] - g_penalty;
            g_dp[i * stride + j] = maxi3(match, del, ins);
        }
    }
    h_score_out[0] = g_dp[n * stride + n];
}

void solution_free(void) {
    free(g_seq_a); g_seq_a = NULL;
    free(g_seq_b); g_seq_b = NULL;
    free(g_dp); g_dp = NULL;
    g_seq_len = 0;
    g_penalty = 0;
}
